package Entidad;

/**
 *
 * @author Jose
 */
public class Curso {
    private int idCurso;
    private String nomCurso;
    private String descripCurso;

    
    /**
     * @return the idCurso
     */
    public int getIdCurso() {
        return idCurso;
    }

    /**
     * @param idCurso the idCurso to set
     */
    public void setIdCurso(int idCurso) {
        this.idCurso = idCurso;
    }

    /**
     * @return the nomCurso
     */
    public String getNomCurso() {
        return nomCurso;
    }

    /**
     * @param nomCurso the nomCurso to set
     */
    public void setNomCurso(String nomCurso) {
        this.nomCurso = nomCurso;
    }

    /**
     * @return the descripCurso
     */
    public String getDescripCurso() {
        return descripCurso;
    }

    /**
     * @param descripCurso the descripCurso to set
     */
    public void setDescripCurso(String descripCurso) {
        this.descripCurso = descripCurso;
    }
    
    
            
}
